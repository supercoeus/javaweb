package com.lsgx.admin.model;

import java.io.Serializable;
import java.util.Date;

/**
 * 项目反馈留言表
 * @author zjy
 *
 */
public class LeaveWord implements Serializable
{
    /**
     * 
     */
    private static final long serialVersionUID = 9116689521250903078L;
    private Integer id;
    private Integer projectFeedback_id;
    private Integer userId;
    private Integer pid;
    private String content;
    private String contentShort;
    private Date createTime;
    
    private String userName;
    public Integer getId()
    {
        return id;
    }
    public void setId(Integer id)
    {
        this.id = id;
    }
    public Integer getProjectFeedback_id()
    {
        return projectFeedback_id;
    }
    public void setProjectFeedback_id(Integer projectFeedback_id)
    {
        this.projectFeedback_id = projectFeedback_id;
    }
    public Integer getUserId()
    {
        return userId;
    }
    public void setUserId(Integer userId)
    {
        this.userId = userId;
    }
    public Integer getPid()
    {
        return pid;
    }
    public void setPid(Integer pid)
    {
        this.pid = pid;
    }
    public String getContent()
    {
        return content;
    }
    public void setContent(String content)
    {
        this.content = content;
    }
    public Date getCreateTime()
    {
        return createTime;
    }
    public void setCreateTime(Date createTime)
    {
        this.createTime = createTime;
    }
    
    public String getUserName()
    {
        return userName;
    }
    
    public void setUserName(String userName)
    {
        this.userName = userName;
    }
    
    public String getContentShort()
    {
        return contentShort;
    }
    
    public void setContentShort(String contentShort)
    {
        this.contentShort = contentShort;
    }
    
}
