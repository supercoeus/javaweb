package com.lsgx.admin.controller.company.vo;

import java.io.Serializable;
import java.util.List;

/**
 * 用户存储excel导入企业信息
 * @author zjy
 *list参数构造方法属性赋值必须和属性顺序一致，属性顺序必须和excel数据一致
 */
public class CompanyExcel implements Serializable
{
    /**
     * 
     */
    private static final long serialVersionUID = 7401526767245932074L;
    
    /**
     * 企业名称
     */
    private String name;
    
    /**
     * 企业地址
     */
    private String address;

    /**
     * 负责人
     */
    private String head;
    
    /**
     * 手机号
     */
    private String mobile;
    
    /**
     * 企业简介
     */
    private String introduction;
    
    /**
     * 余额
     */
    private Double balance;
    
    /**
     * 企业注册号
     */
    private String registerNo;

    /**
     * 组织机构代码
     */
    private String groupCode;
    
    /**
     * 法定代表人姓名
     */
    private String legalName;
    
    /**
     * 身份证
     */
    private String identity;
    
    /**
     * 手机验证状态
     */
    private Integer mobileState;
    
    /**
     * 公司网址
     */
    private String url;
    
    /**
     * 企业类型
     */
    private Integer type;
    
    /**
     * 企业信息
     */
    private String infomation;
    
    /**
     * 企业使用状态
     */
    private Integer useState;
    
    /**
     * 企业认证状态
     */
    private Integer state;
    
    /**
     * 业务范围
     */
    private String serviceField;

    public CompanyExcel()
    {
    }
    
    public CompanyExcel(List<String> values)
    {
        super();
        this.name = values.get(0);
        this.address = values.get(1);
        this.head = values.get(2);
        this.mobile = values.get(3);
        this.introduction = values.get(4);
        this.balance = Double.parseDouble(values.get(5));
        this.registerNo = values.get(6);
        this.groupCode = values.get(7);
        this.legalName = values.get(8);
        this.identity = values.get(9);
        this.mobileState = Integer.parseInt(values.get(10));
        this.url = values.get(11);
        this.type = Integer.parseInt(values.get(12));
        this.infomation = values.get(13);
        this.useState = Integer.parseInt(values.get(14));
        this.state = Integer.parseInt(values.get(15));
        this.serviceField = values.get(16);
    }

    public String getName()
    {
        return name;
    }

    public void setName(String name)
    {
        this.name = name;
    }
    
    public String getAddress()
    {
        return address;
    }
    
    public void setAddress(String address)
    {
        this.address = address;
    }

    public String getHead()
    {
        return head;
    }

    public void setHead(String head)
    {
        this.head = head;
    }
    
    public String getMobile()
    {
        return mobile;
    }
    
    public void setMobile(String mobile)
    {
        this.mobile = mobile;
    }
    
    public String getIntroduction()
    {
        return introduction;
    }
    
    public void setIntroduction(String introduction)
    {
        this.introduction = introduction;
    }
    
    public Double getBalance()
    {
        return balance;
    }
    
    public void setBalance(Double balance)
    {
        this.balance = balance;
    }
    
    public String getRegisterNo()
    {
        return registerNo;
    }
    
    public void setRegisterNo(String registerNo)
    {
        this.registerNo = registerNo;
    }
    
    public String getGroupCode()
    {
        return groupCode;
    }
    
    public void setGroupCode(String groupCode)
    {
        this.groupCode = groupCode;
    }

    public String getLegalName()
    {
        return legalName;
    }

    public void setLegalName(String legalName)
    {
        this.legalName = legalName;
    }
    
    public String getIdentity()
    {
        return identity;
    }
    
    public void setIdentity(String identity)
    {
        this.identity = identity;
    }
    
    public Integer getMobileState()
    {
        return mobileState;
    }
    
    public void setMobileState(Integer mobileState)
    {
        this.mobileState = mobileState;
    }
    
    public String getUrl()
    {
        return url;
    }
    
    public void setUrl(String url)
    {
        this.url = url;
    }

    public Integer getType()
    {
        return type;
    }

    public void setType(Integer type)
    {
        this.type = type;
    }
    
    public String getInfomation()
    {
        return infomation;
    }
    
    public void setInfomation(String infomation)
    {
        this.infomation = infomation;
    }
    
    public Integer getUseState()
    {
        return useState;
    }
    
    public void setUseState(Integer useState)
    {
        this.useState = useState;
    }
    
    public Integer getState()
    {
        return state;
    }
    
    public void setState(Integer state)
    {
        this.state = state;
    }
    
    public String getServiceField()
    {
        return serviceField;
    }
    
    public void setServiceField(String serviceField)
    {
        this.serviceField = serviceField;
    }

}
